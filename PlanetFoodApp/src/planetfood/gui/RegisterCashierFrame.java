    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        int result=validateInput();
         if(result==0)
       {
           JOptionPane.showMessageDialog(null,"please input UserId for Employee","Invalid Input!",JOptionPane.ERROR_MESSAGE);
           return;
       }
       if(result==-1)
       {
           JOptionPane.showMessageDialog(null,"Please input password field for Employee","Invalid Input!",JOptionPane.ERROR_MESSAGE);
           return;
       }
       if(result==-2)
           {
           JOptionPane.showMessageDialog(null,"Please input retype password field for Employee","Invalid Input!",JOptionPane.ERROR_MESSAGE);
           return;
       }
        if(password.equals(password2)==false)
           {
           JOptionPane.showMessageDialog(null,"Password Should match","Invalid Input!",JOptionPane.ERROR_MESSAGE);
           return;
       }
        try
        {
            User u=new User(); 
            u.setEmpId(empId);
            u.setUserid(userId);
            u.setPassword(password);
            u.setUsertype("cashier");
            u.setUserName(empIdList.get(empId));
            boolean status=UserDao.registerCashier(u);
            if(status)
           {
               JOptionPane.showMessageDialog(null,"Cashier successfully Register!","Success!",JOptionPane.INFORMATION_MESSAGE);
               clearAll();
           }
           else
               JOptionPane.showMessageDialog(null,"Cannot register the Cashier","Adding employee Denied!",JOptionPane.ERROR_MESSAGE);
       }
           catch(SQLException ex)
                   {
                   JOptionPane.showMessageDialog(null,"Error while registering Cashier","Exception!",JOptionPane.ERROR_MESSAGE);
                   ex.printStackTrace();
                   }
        
    }//GEN-LAST:event_btnRegisterActionPerformed

